CREATE DATABASE IF NOT EXISTS espiaso;

USE espiaso;

DROP TABLE IF EXISTS DiscCourse;
DROP TABLE IF EXISTS Relation;
DROP TABLE IF EXISTS Discipline;
DROP TABLE IF EXISTS Course;

CREATE TABLE IF NOT EXISTS Discipline (
	Code CHAR(255) NOT NULL,
    Name CHAR(255) NOT NULL,
    Slug CHAR(255) NOT NULL,
    HasContent BOOL DEFAULT False,
    PRIMARY KEY (Code)
);

CREATE TABLE IF NOT EXISTS Course (
	ID INT UNSIGNED NOT NULL AUTO_INCREMENT,
    Name CHAR(255) NOT NULL,
    Slug CHAR(255) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE IF NOT EXISTS DiscCourse (
	CodeDisc CHAR(255) NOT NULL,
    IDCour INT UNSIGNED NOT NULL,
    Semester SMALLINT NOT NULL,
    FOREIGN KEY (CodeDisc) REFERENCES Discipline(Code),
    FOREIGN KEY (IDCour) REFERENCES Course(ID)
);

CREATE TABLE IF NOT EXISTS Relation (
    Code CHAR(255) NOT NULL,
    Dependency CHAR(255) NOT NULL,
    FOREIGN KEY (Code) REFERENCES Discipline(Code)
);

